const fs = require('fs');
const path = require('path');
const { spawn } = require('child_process');
const net = require('net');

const portFile = path.join(__dirname, '.port');
const defaultPort = 51793;

function getPort() {
  if (fs.existsSync(portFile)) {
    return parseInt(fs.readFileSync(portFile, 'utf-8').trim(), 10) || defaultPort;
  }
  return defaultPort;
}

function checkPort(port, callback) {
  const socket = new net.Socket();
  socket.setTimeout(500);
  socket.on('connect', () => {
    socket.destroy();
    callback(true);
  });
  socket.on('timeout', () => {
    socket.destroy();
    callback(false);
  });
  socket.on('error', () => {
    socket.destroy();
    callback(false);
  });
  socket.connect(port, 'localhost');
}

function waitAndLaunch() {
  console.log('Waiting for .port file to be generated by Vite...');
  
  const checkInterval = setInterval(() => {
    if (fs.existsSync(portFile)) {
      clearInterval(checkInterval);
      const port = getPort();
      console.log(`Detected Vite running on port: ${port}. Waiting for server to be ready...`);
      
      const waitInterval = setInterval(() => {
        checkPort(port, (ready) => {
          if (ready) {
            clearInterval(waitInterval);
            console.log(`Vite server is ready on port ${port}. Launching Electron...`);
            
            // Spawn electron .
            const child = spawn('npx', ['electron', '.'], {
              stdio: 'inherit',
              shell: true
            });
            child.on('exit', (code) => {
              process.exit(code || 0);
            });
          }
        });
      }, 500);
    }
  }, 200);
}

// Ensure old .port file is deleted on start
if (fs.existsSync(portFile)) {
  fs.unlinkSync(portFile);
}

waitAndLaunch();
